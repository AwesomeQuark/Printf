#include "ft_printf.h"

size_t		ft_strcat2(char	*s1, char *s2)
{
	size_t	size;

	size = 0;
	while (*s1)
		s1++;
	while (*s1)
	{
		*s1 = *s2;
		s1++;
		s2++;
		size++;
	}
	*s1 = '\0';
	return (size);
}
